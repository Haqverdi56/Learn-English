export const getCurrentUserRank = (userId) => {
	const sortedUsers = [...leaderboardData].sort((a, b) => b.score - a.score);
	return sortedUsers.findIndex(user => user.id === userId) + 1;
};

export const getTopUsers = (count = 10) => {
	return [...leaderboardData].sort((a, b) => b.score - a.score).slice(0, count);
};